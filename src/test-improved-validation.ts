import fs from 'fs';
import path from 'path';
import { ImprovedImageValidationService } from './services/improvedImageValidationService';

/**
 * Test du service de validation am√©lior√©
 */

async function testImprovedValidation() {
  console.log('üß™ Test du service de validation am√©lior√© (sans TensorFlow)\n');

  const improvedService = new ImprovedImageValidationService();
  
  // Afficher les statistiques du service
  const stats = improvedService.getStats();
  console.log('üìä Statistiques du service am√©lior√©:');
  console.log(`   üåø PlantNet: ${stats.plantnetAvailable ? 'Cl√© API disponible' : 'Pas de cl√© API'}`);
  console.log(`   üéØ Seuil de confiance: ${(stats.minAgriculturalConfidence * 100).toFixed(1)}%`);
  console.log(`   üå± Vert minimum: ${stats.minGreenPercentage}%`);
  console.log(`   üè† Uniformit√© max (pelouse): ${(stats.maxUniformGreen * 100).toFixed(1)}%`);
  console.log(`   üñºÔ∏è  Complexit√© texture min: ${(stats.minTextureComplexity * 100).toFixed(1)}%`);
  console.log('');

  // Tester avec les vraies images
  const testCases = [
    {
      filename: 'mais.jpg',
      expectedType: 'agricultural',
      description: 'Plant de ma√Øs'
    },
    {
      filename: 'cacao.jpg',
      expectedType: 'agricultural',
      description: 'Plant de cacao'
    },
    {
      filename: 'chenille_sur_mais.jpg',
      expectedType: 'agricultural',
      description: 'Chenille sur ma√Øs'
    },
    {
      filename: 'Mais_detruite_ravageur.jpg',
      expectedType: 'agricultural',
      description: 'Ma√Øs d√©truit par ravageur'
    },
    {
      filename: 'caco_malade.jpg',
      expectedType: 'agricultural',
      description: 'Cacao malade'
    },
    {
      filename: 'maison_villa_avec_gazon.jpeg',
      expectedType: 'non-agricultural',
      description: 'Maison avec gazon (test critique)'
    }
  ];

  const results: any[] = [];
  const imagesPath = path.join(process.cwd(), 'images', 'test');

  for (const testCase of testCases) {
    const imagePath = path.join(imagesPath, testCase.filename);
    
    console.log(`üîç Test: ${testCase.description}`);
    console.log(`üìÅ Fichier: ${testCase.filename}`);
    
    if (!fs.existsSync(imagePath)) {
      console.log(`‚ùå Image non trouv√©e: ${imagePath}\n`);
      continue;
    }

    try {
      const imageBuffer = fs.readFileSync(imagePath);
      console.log(`üìä Taille: ${(imageBuffer.length / 1024).toFixed(1)} KB`);

      const startTime = Date.now();
      const validationResult = await improvedService.validateAgriculturalImage(imageBuffer);
      const processingTime = Date.now() - startTime;

      // V√©rifier si le r√©sultat est correct
      const isCorrect = (testCase.expectedType === 'agricultural') === validationResult.isValid;
      const status = isCorrect ? '‚úÖ' : '‚ùå';
      
      console.log(`${status} R√©sultat: ${validationResult.isValid ? 'AGRICOLE' : 'NON-AGRICOLE'}`);
      console.log(`üìà Confiance: ${(validationResult.confidence * 100).toFixed(1)}%`);
      console.log(`‚è±Ô∏è  Temps: ${processingTime}ms`);
      console.log(`üìã Raisons: ${validationResult.reasons.join(', ')}`);

      // Afficher les d√©tails des analyses
      if (validationResult.sources.colorAnalysis) {
        const color = validationResult.sources.colorAnalysis;
        console.log(`üé® Couleurs d√©taill√©es:`);
        console.log(`   üåø Vert total: ${color.greenPercentage.toFixed(1)}%`);
        console.log(`   üå≤ Vert fonc√©: ${color.darkGreenPercentage.toFixed(1)}%`);
        console.log(`   üå± Vert clair: ${color.lightGreenPercentage.toFixed(1)}%`);
        console.log(`   üü§ Brun: ${color.brownPercentage.toFixed(1)}%`);
        console.log(`   üîµ Bleu: ${color.bluePercentage.toFixed(1)}%`);
        console.log(`   üìè Uniformit√© vert: ${(color.greenUniformity * 100).toFixed(1)}%`);
      }

      if (validationResult.sources.textureAnalysis) {
        const texture = validationResult.sources.textureAnalysis;
        console.log(`üñºÔ∏è  Texture d√©taill√©e:`);
        console.log(`   üîß Complexit√©: ${(texture.complexity * 100).toFixed(1)}%`);
        console.log(`   üìê Uniformit√©: ${(texture.uniformity * 100).toFixed(1)}%`);
        console.log(`   ‚ö° Force contours: ${texture.edgeStrength.toFixed(1)}`);
      }

      if (validationResult.sources.plantnet) {
        const pn = validationResult.sources.plantnet;
        console.log(`üåø PlantNet: ${pn.isPlant ? 'Plante' : 'Non-plante'} (${(pn.confidence * 100).toFixed(1)}%)`);
        if (pn.species && pn.species.length > 0) {
          console.log(`   Esp√®ces: ${pn.species.map((s: any) => s.species?.scientificNameWithoutAuthor || 'Inconnue').join(', ')}`);
        }
      }

      if (!validationResult.isValid) {
        console.log(`üí° Type d'erreur: ${validationResult.errorType}`);
        if (validationResult.suggestion) {
          console.log(`üîß Suggestion: ${validationResult.suggestion}`);
        }
      }

      results.push({
        filename: testCase.filename,
        expected: testCase.expectedType,
        actual: validationResult.isValid ? 'agricultural' : 'non-agricultural',
        confidence: validationResult.confidence,
        isCorrect,
        processingTime,
        sources: validationResult.sources
      });

      console.log('');

    } catch (error: any) {
      console.error(`‚ùå Erreur: ${error.message}\n`);
      results.push({
        filename: testCase.filename,
        expected: testCase.expectedType,
        actual: 'error',
        confidence: 0,
        isCorrect: false,
        processingTime: 0,
        error: error.message
      });
    }
  }

  // Afficher le r√©sum√©
  displayImprovedTestSummary(results);
}

function displayImprovedTestSummary(results: any[]) {
  console.log('üìä R√âSUM√â DES TESTS AM√âLIOR√âS\n');
  console.log('='.repeat(60));

  const totalTests = results.length;
  const correctResults = results.filter(r => r.isCorrect).length;
  const errorResults = results.filter(r => r.actual === 'error').length;
  const accuracy = totalTests > 0 ? (correctResults / (totalTests - errorResults)) * 100 : 0;

  console.log(`üìà Total des tests: ${totalTests}`);
  console.log(`‚úÖ R√©sultats corrects: ${correctResults}`);
  console.log(`‚ùå Erreurs: ${errorResults}`);
  console.log(`üéØ Pr√©cision: ${accuracy.toFixed(1)}%\n`);

  // Comparaison avec les syst√®mes pr√©c√©dents
  console.log('üîÑ COMPARAISON AVEC LES SYST√àMES PR√âC√âDENTS:');
  console.log('   Syst√®me basique (couleurs): 87.5% de pr√©cision');
  console.log(`   Syst√®me am√©lior√©: ${accuracy.toFixed(1)}% de pr√©cision`);
  
  if (accuracy > 87.5) {
    console.log('   üéâ AM√âLIORATION! Le syst√®me am√©lior√© est plus pr√©cis');
  } else if (accuracy === 87.5) {
    console.log('   ‚öñÔ∏è  √âGALIT√â - M√™me pr√©cision');
  } else {
    console.log('   ‚ö†Ô∏è  R√âGRESSION - Le syst√®me basique √©tait plus pr√©cis');
  }
  console.log('');

  // Temps de traitement
  const validResults = results.filter(r => r.actual !== 'error');
  if (validResults.length > 0) {
    const avgProcessingTime = validResults.reduce((sum, r) => sum + r.processingTime, 0) / validResults.length;
    console.log(`‚è±Ô∏è  Temps moyen: ${avgProcessingTime.toFixed(1)}ms`);
    console.log('   (Compar√© √† ~104ms pour le syst√®me basique)');
  }

  console.log('');

  // D√©tails par image
  console.log('üìã D√âTAILS PAR IMAGE:');
  results.forEach(result => {
    const status = result.isCorrect ? '‚úÖ' : (result.actual === 'error' ? 'üí•' : '‚ùå');
    console.log(`${status} ${result.filename}: ${result.expected} ‚Üí ${result.actual} (${(result.confidence * 100).toFixed(1)}%)`);
    
    if (result.error) {
      console.log(`   üí• Erreur: ${result.error}`);
    }
  });

  console.log('');

  // Analyse des faux positifs/n√©gatifs
  const falsePositives = results.filter(r => r.expected === 'non-agricultural' && r.actual === 'agricultural');
  const falseNegatives = results.filter(r => r.expected === 'agricultural' && r.actual === 'non-agricultural');

  if (falsePositives.length === 0 && falseNegatives.length === 0) {
    console.log('üéâ PARFAIT! Aucun faux positif ou n√©gatif d√©tect√©.');
  } else {
    if (falsePositives.length > 0) {
      console.log(`üö® FAUX POSITIFS (${falsePositives.length}): Images non-agricoles d√©tect√©es comme agricoles`);
      falsePositives.forEach(fp => {
        console.log(`   ‚Ä¢ ${fp.filename}`);
        if (fp.sources && fp.sources.colorAnalysis) {
          const color = fp.sources.colorAnalysis;
          console.log(`     Uniformit√© vert: ${(color.greenUniformity * 100).toFixed(1)}%, Vert clair: ${color.lightGreenPercentage.toFixed(1)}%`);
        }
      });
    }

    if (falseNegatives.length > 0) {
      console.log(`üö® FAUX N√âGATIFS (${falseNegatives.length}): Images agricoles rejet√©es`);
      falseNegatives.forEach(fn => console.log(`   ‚Ä¢ ${fn.filename}`));
    }
  }

  console.log('\nüí° RECOMMANDATIONS:');
  if (accuracy >= 95) {
    console.log('   ‚ú® Excellent! Le syst√®me est pr√™t pour la production');
  } else if (accuracy >= 90) {
    console.log('   üëç Tr√®s bon! Quelques ajustements mineurs possibles');
  } else if (accuracy >= 80) {
    console.log('   ‚ö†Ô∏è  Correct mais peut √™tre am√©lior√©');
    console.log('   ‚Ä¢ Consid√©rer l\'ajout d\'une cl√© PlantNet API');
    console.log('   ‚Ä¢ Ajuster les seuils de d√©tection de pelouse');
  } else {
    console.log('   üîß N√©cessite des am√©liorations importantes');
    console.log('   ‚Ä¢ Revoir les seuils de confiance');
    console.log('   ‚Ä¢ Am√©liorer la d√©tection de pelouse');
    console.log('   ‚Ä¢ Ajouter une cl√© PlantNet API');
  }

  // Test sp√©cifique pour la pelouse
  const lawnTest = results.find(r => r.filename.includes('gazon') || r.filename.includes('maison'));
  if (lawnTest) {
    console.log('\nüè† ANALYSE SP√âCIFIQUE PELOUSE:');
    if (lawnTest.actual === 'non-agricultural') {
      console.log('   ‚úÖ Pelouse correctement rejet√©e');
    } else {
      console.log('   ‚ùå Pelouse incorrectement accept√©e');
      console.log('   üîß Suggestions d\'am√©lioration:');
      console.log('   ‚Ä¢ R√©duire le seuil d\'uniformit√© verte');
      console.log('   ‚Ä¢ Augmenter la p√©nalit√© pour vert clair');
      console.log('   ‚Ä¢ Am√©liorer la d√©tection de texture uniforme');
    }
  }
}

